package model;

public class Apartamento {

	private int numero;
	private int quantQuarto;
	private String tipoOcupacao;

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public int getQuantQuarto() {
		return quantQuarto;
	}

	public void setQuantQuarto(int quantQuarto) {
		this.quantQuarto = quantQuarto;
	}

	public String getTipoOcupacao() {
		return tipoOcupacao;
	}

	public void setTipoOcupacao(String tipoOcupacao) {
		this.tipoOcupacao = tipoOcupacao;
	}
	

}
